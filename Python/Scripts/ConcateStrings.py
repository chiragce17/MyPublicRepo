# Using the + operator

str1 = "Hello"
str2 = "World"
str = str1 + ' ' + str2
print(str)


# Using the Join function
str1 = "Hello"
str2 = "Python"
str3 = "World"
str = ','.join([str1,str2,str3])
print(str)

# Concat list.
list = ["Dog","cat","cow"]
str = ' '.join(list)
print(str)

# Concat tupple 
wild_animals = ('Tiger','Lion','Fox')
str = ' '.join(wild_animals)
print(str)